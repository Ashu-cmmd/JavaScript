const month = "May"

switch (month) {
    case "january":
        console.log("january")
        break;
    case "feb":
        console.log("feb")
        break;
    case "march":
        console.log("march")
        break;
    case "April":
        console.log("April")
        break;
    case "May":
        console.log("May")
        break;
    case June:
        console.log("June")
        break;

    default:
        console.log("default case match");
        
        break;
}

// if we do't use "break" after the match it will execute all case except default case .
